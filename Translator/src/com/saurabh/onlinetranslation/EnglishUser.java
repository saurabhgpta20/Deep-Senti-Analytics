public class EnglishUser {
	private int sno;
	private String english_tweet;
	private String irish_translate;
	private int score1;

	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getEnglish_tweet() {
		return english_tweet;
	}
	public void setEnglish_tweet(String english_tweet) {
		this.english_tweet = english_tweet;
	}
	public String getIrish_translate() {
		return irish_translate;
	}
	public void setIrish_translate(String irish_translate) {
		this.irish_translate = irish_translate;
	}
	public int getScore1() {
		return score1;
	}
	public void setScore1(int score1) {
		this.score1 = score1;
	}
}
