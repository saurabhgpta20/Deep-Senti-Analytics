package com.saurabh.onlinetranslation;

public class IrishUser {
	private int sno;
	private String irish_tweet;
	private String eng_translate;
	private int score1;
	
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getIrish_tweet() {
		return irish_tweet;
	}
	public void setIrish_tweet(String irish_tweet) {
		this.irish_tweet = irish_tweet;
	}
	public String getEng_translate() {
		return eng_translate;
	}
	public void setEng_translate(String eng_translate) {
		this.eng_translate = eng_translate;
	}
	public int getScore1() {
		return score1;
	}
	public void setScore1(int score1) {
		this.score1 = score1;
	}
}
